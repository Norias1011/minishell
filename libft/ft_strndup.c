#include "libft.h"

char	*ft_strndup(const char *s, size_t n, t_garbage **gc)
{
	char	*dup;
	size_t	i;

	i = 0;
	dup = g_malloc(gc, (n + 1) * sizeof(char));
	if (!dup)
		return (NULL);
	while (s[i] && i < n)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
